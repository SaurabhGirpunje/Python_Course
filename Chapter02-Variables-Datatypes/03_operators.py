# 1.Arithmetic operators: +, -, *, / etc.
# 2.Assignment operators: =, +=, -= etc.
# 3.Comparison operators: ==, >, >=, <, != etc.
# 4.Logical operators: and, or, not.

# Arithmetic operators
a = 7
b = 8
print(a+b)


# Assignment operators
a = 8
b = 2
b+=a
print(b)


# Comparison operators
print(5<4)
print(10 != 0)

d = 5 == 5
print(d)


# Logical operators
print(True and False)